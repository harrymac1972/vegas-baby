// Function to change the text of the 'customizeBtn' element
function changeText(button) {
    // Set the inner text of 'customizeBtn' to 'Build your Own'
    document.getElementById('customizeBtn').innerText = 'Build your Own';
}

// Function to restore the text of the 'customizeBtn' element
function restoreText(button) {
    // Set the inner text of 'customizeBtn' to 'Customize'
    document.getElementById('customizeBtn').innerText = 'Customize';
}